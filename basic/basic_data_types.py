# === Basic Data Types === #

first_text = "This is a String" # Double Quotes
second_text = 'This is also a String' # Single Quotes

print("First Text Type:", type(first_text))
print("Second Text Type:", type(second_text))

first_integer = 100 # Without decimal separator
first_float = 3.14 # The PI number is a Float!

print("First Integer Type:", type(first_integer))
print("First Float Type:", type(first_float))
